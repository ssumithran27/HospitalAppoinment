package org.example.model;

public class LoggingUser {
    private long loggingId;
    private String loggingName;
    private String loggingPassword;

    public long getLoggingId() {
        return loggingId;
    }

    public void setLoggingId(long loggingId) {
        this.loggingId = loggingId;
    }

    public String getLoggingName() {
        return loggingName;
    }

    public void setLoggingName(String loggingName) {
        this.loggingName = loggingName;
    }

    public String getLoggingPassword() {
        return loggingPassword;
    }

    public void setPassword(String loggingPassword) {
        this.loggingPassword = loggingPassword;
    }
}
